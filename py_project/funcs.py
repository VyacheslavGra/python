
def sum_number():
    number_1 = int(input('Введите число'))
    number_2 = int(input('Введите число'))

    sum_numbers = number_1 + number_2

    print(f"{sum_numbers}")

def dif_number():
    number_1 = int(input('Введите число'))
    number_2 = int(input('Введите число'))

    dif_numbers = number_1 - number_2

    print(f"{dif_numbers}")